def test_gen():
    test = [None, None, None]
    test[0] = choice([i for i in data.keys()])
    test[1] = option_gen(data[test[0]])
    return test


def option_gen(corr):
    out = [corr]
    val = [i for i in data.values()]
    for i in range(3):
        out.append(val.pop(val.index(choice(val))))
    return randomizer(out)


def randomizer(ops):
    out = list()
    for i in range(len(ops)):
        out.append(ops.pop(ops.index(choice(ops))))
    return out


def get_key(val):
    return [key for key, value in data.items() if value == val]


