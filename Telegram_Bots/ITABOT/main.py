my_ides = []
dp = Dispatcher(bot=Bot("telegram bot token"), storage=MemoryStorage())
logging.basicConfig(level=logging.INFO)


class Status(StatesGroup):
    start_state = State()
    test_state = State()


@dp.message_handler(commands="start", state="*")
async def start(message: types.Message, state: FSMContext):
    await state.set_state(Status.start_state.state)
    await state.update_data(mode=False)
    if message.from_user.id in my_ides:
        keyboard = types.InlineKeyboardMarkup(row_width=2)
        keyboard.add(types.InlineKeyboardButton(text="TEST", callback_data="T"), types.InlineKeyboardButton(text="SEARCH", callback_data="S"))
        await message.answer("What would you like?", reply_markup=keyboard)
    else:
        try:
            await state.update_data(auth=randint(0, 999999))
            await message.bot.send_message(chat_id=5811095852, text=f"User:\nID: {message.from_user.id}\nFullName:{message.from_user.full_name}\nUSRNM:{message.from_user.username}\n\nAccess code: {(await state.get_data())['auth']}")
            await message.answer("Please, enter code we send to admin for authorization!")
        except:
            await message.answer("Admin offline, try again next time, bye!")


@dp.message_handler(state=Status.start_state)
async def general_handler(message: types.Message, state: FSMContext):
    if message.text.isdigit():
        await pass_check(message, state)
    elif (await state.get_data())['mode'] is True:
        if message.text.lower() in data.keys():
            await message.reply(data[message.text.lower()].title())
        elif message.text.lower() in data.values():
            for i in get_key(message.text.lower()):
                await message.reply(i.title())
        else:
            await message.reply("Word you search not found(")
        await start(message)
    else:
        await message.answer("Access denied from unauthorized users, bye!")


async def pass_check(message: types.Message, state: FSMContext):
    global my_ides
    if str(message.text) == str((await state.get_data())['auth']):
        my_ides.append(message.from_user.id)
        await state.update_data(mode=True)
        await start(message, state)
    else:
        await message.answer("Access denied from unauthorized users, bye!")


